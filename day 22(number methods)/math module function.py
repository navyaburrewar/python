## here we must import math

#math.ceil() – rounds UP
import math
print(math.ceil(3.2)) 


#math.floor() – rounds DOWN
print(math.floor(3.9))  


#math.sqrt() – square root
print(math.sqrt(16))    

##math.exp() – e to the power x (eˣ)
print(math.exp(3))        # (eˣ)


#math.log() – natural log (base e)
print(math.log(10))

