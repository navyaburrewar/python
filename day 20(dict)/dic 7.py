
## dict methods

## keys()         ## returns keys
## values()       ## returns all values
## items()        ## key value pairs
## updates()      ## merge dictionaries
## copy()         ## copy dictionary
marks ={
    "navya" :10,
    "pandu":20,        
    "vivek":40
}
print(marks.keys())
print(marks.values())
print(marks.items())
marks.update({"nandhu" : 43})
print(marks.copy())