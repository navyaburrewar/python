#What is a while loop?

#A while loop is used to repeat a block of code as long as a condition is True.
#You don’t know in advance how many times the loop will run
#The loop depends on a condition

# syntax

# while condition:
    #code to be executed


#Important points:

#condition must be True or False
#The code inside the loop must be indented
#The loop stops when the condition becomes False




# Flow of execution (how it works)....

# Python checks the condition
# If the condition is True → loop body executes
# After execution, Python again checks the condition
# This repeats until the condition becomes False