#Arithmetic Operations (Math Operations)
#Used to perform mathematical calculations.
a = 10
b = 3

print(a + b)   # Addition
print(a - b)   # Subtraction
print(a * b)   # Multiplication
print(a / b)   # Division
print(a % b)   # Remainder
print(a ** b)  # Power
print(a // b)  # Floor division(removes decimal part)
