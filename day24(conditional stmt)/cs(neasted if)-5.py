# nested if
# An if statement inside another if statement

# It is used when:
# One condition depends on another condition
# You want to check multiple related conditions step-by-step

#We use nested if when:
#The second condition should be checked only if the first condition is True

## syntax 

# if condition:
#     if condition:
#         statment
#     else:
#         statment
# else:
#     statment            


#Important rules

#Indentation (spaces) is very important in Python
#Inner if runs only if the outer if is True