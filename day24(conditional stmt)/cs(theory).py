#1. What are Conditional Statements?

# Conditional statements are used in programming to make decisions.
# They allow a program to execute different blocks of code based on conditions (true or false).

#   “If a condition is true, do this; otherwise, do something else.”



#2. Why Are Conditional Statements Needed?


#Without conditional statements:
#Programs would run in only one direction
#No decision-making would be possible
#With conditional statements:
#Programs can respond to different inputs
#Logic and intelligence are added to programs

#Example uses:
#Checking pass or fail
#Login authentication
#Comparing numbers
#Menu-driven programs